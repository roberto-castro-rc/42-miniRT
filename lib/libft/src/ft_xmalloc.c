#include "libft.h"

void	*ft_xmalloc(size_t size)
{
	void	*ptr;

	ptr = malloc(size);
	if (!ptr)
	{
		ft_putstr_fd("Fatal: memory allocation failed\n", 2);
		exit(1);
	}
	return (ptr);
}
