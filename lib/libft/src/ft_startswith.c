#include "libft.h"

int	ft_startswith(const char *str, const char *prefix)
{
	if (!str || !prefix)
		return (0);
	while (*prefix)
	{
		if (*prefix != *str)
			return (0);
		prefix++;
		str++;
	}
	return (1);
}
